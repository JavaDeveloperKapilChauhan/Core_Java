package _9_if_else;

public class If_else {

    public static void main(String [] args){

        int x = 18 ;

        if(x<=20){
            System.out.println("Number is less than 21") ;
        }
        else if(x<=50){
            System.out.println("Number is less than 51") ;
        }
        else{
            System.out.println("Number is greater than 50") ;
        }
    }


    
}
